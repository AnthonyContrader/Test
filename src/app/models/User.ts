
export class User {
    userId: number;
    userUser: string;
    userPass: string;
    userType: string;
    


    // tslint:disable-next-line:max-line-length
    constructor(userId: number, userUser: string, userPass: string, userType: string) {
        this.userId = userId;
        this.userUser = userUser;
        this.userPass = userPass;
        this.userType = userType;
        
    }

}
